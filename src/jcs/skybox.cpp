SkyBox::SkyBox(Renderer &renderer, uint texture) : renderer{renderer},
                                                   texture{texture} {
    glGenVertexArrays(1, &vao);
    glBindVertexArray(vao);

    uint vbo;
    glGenBuffers(1, &vbo);
    glBindBuffer(GL_ARRAY_BUFFER, vbo);
    glBufferData(GL_ARRAY_BUFFER, sizeof(SKYBOX_VERTICES), &SKYBOX_VERTICES,
                 GL_STATIC_DRAW);
    glEnableVertexAttribArray(0);
    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 3 * sizeof(float), nullptr);
}

void SkyBox::draw(const glm::mat4 &view) const {
    glDepthMask(GL_FALSE);
    glDepthFunc(GL_LEQUAL);

    renderer.skybox_shader.use();
    renderer.skybox_shader.setView(view);
    renderer.skybox_shader.setProjection(renderer.proj_3d);

    glBindVertexArray(vao);

    glActiveTexture(GL_TEXTURE0);
    glBindTexture(GL_TEXTURE_CUBE_MAP, texture);

    glDrawArrays(GL_TRIANGLES, 0, 36);

    glBindVertexArray(0);
    glDepthFunc(GL_LESS);
    glDepthMask(GL_TRUE);
}
